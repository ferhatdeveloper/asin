import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const mobileRoot = path.resolve(__dirname, '..');
const menuPath = path.join(mobileRoot, 'src/config/menuConfig.ts');
const src = fs.readFileSync(menuPath, 'utf8');

function extractPairs(re) {
  const out = [];
  let m;
  while ((m = re.exec(src))) out.push({ id: m[1], label: m[2] });
  return out;
}

const sections = extractPairs(/id:\s*'([^']+)',\s*title:\s*'([^']*)'/g);
const items = extractPairs(
  /id:\s*'([^']+)',\s*label:\s*'([^']*)'(?:\s*,\s*screen:\s*'[^']+'|\s*,\s*badge:|\s*,\s*children:)/g,
);
const quickBlock = src.slice(src.indexOf('QUICK_ACCESS'), src.indexOf('MENU_SECTIONS'));
const quick = [];
{
  const re = /id:\s*'([^']+)',\s*label:\s*'([^']*)'/g;
  let m;
  while ((m = re.exec(quickBlock))) quick.push({ id: m[1], label: m[2] });
}

const uniqById = (rows) => {
  const seen = new Set();
  const out = [];
  for (const r of rows) {
    if (seen.has(r.id)) continue;
    seen.add(r.id);
    out.push(r);
  }
  return out;
};

const sectionMap = Object.fromEntries(uniqById(sections).map((r) => [r.id, r.label]));
const itemMap = Object.fromEntries(uniqById(items).map((r) => [r.id, r.label]));
const quickMap = Object.fromEntries(uniqById(quick).map((r) => [r.id, r.label]));


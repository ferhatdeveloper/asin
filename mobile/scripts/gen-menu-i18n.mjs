function buildMenu(langMap, sectionOverrides = {}) {
  const mapOr = (srcMap) => {
    const out = {};
    for (const [id, trLabel] of Object.entries(srcMap)) {
      out[id] = langMap[id] ?? trLabel;
    }
    return out;
  };
  const sections = mapOr(sectionMap);
  for (const [id, label] of Object.entries(sectionOverrides)) {
    sections[id] = label;
  }
  return {
    sections,
    items: mapOr(itemMap),
    quick: mapOr(quickMap),
    badgeNew: langMap.__badgeNew ?? 'Yeni',
    badgeAi: 'AI',
    groupsCount: langMap.__groupsCount ?? '{{count}} menü grubu',
    moduleShortcuts: langMap.__moduleShortcuts ?? 'Modül kısayolları',
  };
}

